void OLED_init()
{
	I2C_Generate_START();
	
	I2C_Send_SlaveAddress_Write(OLED_ADD);
	
	I2C_Send_byte(0x00);     // next bytes are commands
	
	I2C_Send_byte(0xAE);     // Display OFF
	
	I2C_Send_byte(0x20);     // set  addressing mode
	I2C_Send_byte(0x00);     // horizontal addressing mode
	I2C_Send_byte(0xA8);     // set multiplex ratio, total row lines=64
	I2C_Send_byte(0x3F);
	
	I2C_Send_byte(0x8D);    // enable internal charge up
	I2C_Send_byte(0x14);
	
	I2C_Send_byte(0x40|0x00);   // set start line as row 0
	
	I2C_Send_byte(0xA1);        // set column 0 as left most
	
	I2C_Send_byte(0xC8);        //  set row0 as top
	
	I2C_Send_byte(0xD3);        //set page0 at top of OLED
	I2C_Send_byte(0x00);
	
	I2C_Send_byte(0xD5);        // set frequency of display
	I2C_Send_byte(0x80);
	
	I2C_Send_byte(0xD9);        // set visible,stable and correctly mapped display
	I2C_Send_byte(0xF1);
	I2C_Send_byte(0xDA);
	I2C_Send_byte(0x12);
	I2C_Send_byte(0xDB);
	I2C_Send_byte(0x40);
	
	I2C_Send_byte(0x81);        // set contrast
	I2C_Send_byte(0x7F);
	
	I2C_Send_byte(0xA6);        // set normal display, bit0-OFF, bit1-ON
	
	I2C_Send_byte(0xA4);        // display what's on RAM
	
	I2C_Send_byte(0xAF);        // display ON
	
	I2C_Generate_STOP();
}

void OLED_print_char(char ch, uint8_t page, uint8_t col)     // printing a character by sending entire buffer array- 1024 bytes
{
	// modify the buffer array to send character ch
	for(uint8_t i=0;i<5;i++)      //change 5 bytes--> character width
	{
		buffer[page*128 + col +i]= font_array[ch-32][i];    // write bytes for character ch : ch-32 means the row number of ch
	}
	
	I2C_Generate_START();
	
	I2C_Send_SlaveAddress_Write(OLED_ADD);
	
	I2C_Send_byte(0x40);     // data mode
	for(uint16_t i=0;i<1024;i++)     // send modified buffer
	{
		I2C_Send_byte(buffer[i]);
	}
	
	I2C_Generate_STOP();
	
}

void OLED_print_string(char *str,uint8_t page, uint8_t col)
{
	// modify buffer
	while(*str)     // loop through each characters
	{
		
		for(uint8_t i=0;i<5;i++)    // write 5 bytes of *str
		{
			buffer[page*128+col+i]=font_array[*str-32][i];
		}
		col=col+7;    // character width+space for next character
		str++;
		
		if(col+5>127)    //  if there's no space for new character,
		{   page++;      //    move to beginning of next page
			col=0;
		}
	}
	I2C_Generate_START();
	I2C_Send_SlaveAddress_Write(OLED_ADD);
	
	I2C_Send_byte(0x40);     // data mode
	
	for(uint16_t i=0;i<1024;i++)    // sent entire buffer
	{
		I2C_Send_byte(buffer[i]);
	}
	I2C_Generate_STOP();
}

void OLED_print_signed_num(int32_t num,uint16_t page,uint16_t col)
{
	char buf[12];     // array to hold num string
	ltoa(num,buf,10);    // convert decimal num to a string
	
	OLED_print_string(buf,page,col);
}
void OLED_print_HEXnum(uint8_t num,uint16_t page,uint16_t col)
{
	char buf[12];    // array to hold num string
	itoa(num,buf,16);    // convert decimal num to a string
	OLED_print_string("0x",page,col);
	OLED_print_string(buf,page,col+10);
	
}
void OLED_clear_space(uint8_t page, uint8_t col, uint8_t space)    // for clearing area in a page before printing new characters
{
	for(uint8_t i=0;i<space;i++)
	{
		buffer[page*128 +col+i]=0x00;     // clear bytes in the space
	}
	
}
void OLED_clear_display()    // for clearing full display
{
	for(uint16_t i=0;i<1024;i++)    // clear entire buffer
	{
		buffer[i]=0x00;
	}
	
	I2C_Generate_START();
	I2C_Send_SlaveAddress_Write(OLED_ADD);
	

	I2C_Send_byte(0x40);
	for(uint16_t i=0;i<1024;i++)
	{
		I2C_Send_byte(buffer[i]);    // clear bytes
	}
	I2C_Generate_STOP();
}

void OLED_reset_cursor()
{
	I2C_Generate_START();
	I2C_Send_SlaveAddress_Write(OLED_ADD);
	// reset the cursor to top-left
	I2C_Send_byte(0x00);    // command mode
	
	I2C_Send_byte(0x21);    // reset the column address
	
	I2C_Send_byte(0);       // start at column 0
	I2C_Send_byte(127);     // end at column 127
	
	I2C_Send_byte(0x22);    // reset the page address
	 
	I2C_Send_byte(0);       // start at page 0
	I2C_Send_byte(7);       // end at page 7
	
	I2C_Generate_STOP();
}